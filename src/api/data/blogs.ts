import {BookOpen, Wrench, Cpu, ShoppingCart, Newspaper} from "lucide-react";

/* ============================================================================
 * BLOG DATA MODEL
 * Shared between the blog list (Blogs.tsx) and the single article page
 * (SingleBlog.tsx). The list endpoint returns a trimmed shape; the single
 * endpoint additionally returns `slug` + `content` (structured article body).
 * ========================================================================== */

export interface Post {
    id: number;
    category: string;
    catIcon: typeof Wrench;
    title: string;
    excerpt: string;
    author: string;
    date: string;
    readTime: string;
    featured?: boolean;
    slug?: string;
    content?: ContentBlock[];
}

export interface Category {
    label: string;
    Icon: typeof Wrench;
}

/* Structured article body. Rendered by SingleBlog.tsx in document order.
   Each block type maps to a distinct presentational treatment. */
export type ContentBlock =
    | { type: "paragraph"; text: string }
    | { type: "heading"; text: string }
    | { type: "quote"; text: string; cite?: string }
    | { type: "list"; ordered?: boolean; items: string[] }
    | { type: "callout"; title: string; text: string };

